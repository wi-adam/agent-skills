def ensure_parent(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)


def write_rendered_file(path: Path, content: str, check: bool) -> bool:
    if path.exists():
        current = path.read_text(encoding="utf-8")
        if current == content:
            return False
    if check:
        return True
    ensure_parent(path)
    path.write_text(content, encoding="utf-8")
    return True


def run_golden_tests(operations: dict[str, Any], adapters: dict[str, dict[str, Any]]) -> None:
    github = adapters["github"]
    tkt = adapters["tkt"]

    assert validate_value(operations, github, "ticket_id", "#42") == "42"
    try:
        validate_value(operations, github, "shell_safe_title", "bad $ title")
    except ValueError:
        pass
    else:
        raise AssertionError("shell_safe_title accepted a shell metacharacter")

    github_body = render_formatter(
        operations,
        github,
        "review_body",
        {
            "title": "Review title",
            "summary": "Implemented the thing.",
            "linked_ticket_ids": ["42", "55"],
            "branch_ref": "feature/auth",
        },
    )
    assert "#42, #55" in github_body
    assert "Closes #42, #55" in github_body

    tkt_dependency = render_formatter(
        operations,
        tkt,
        "dependency_annotation",
        {"ticket_ids": ["AUTH-42", "AUTH-55"]},
    )
    assert tkt_dependency == "Depends on AUTH-42, AUTH-55"

    try:
        render_formatter(
            operations,
            github,
            "dependency_annotation",
            {"ticket_ids": []},
        )
    except ValueError:
        pass
    else:
        raise AssertionError("empty ticket_id_list was accepted")

    tkt_publish = command_recipe(
        operations,
        tkt,
        "work_publish",
        {
            "title": "Review title",
            "body_file": ".agent-skills-tmp/review.md",
            "linked_ticket_ids": ["AUTH-42"],
        },
    )
    assert "For each item in `linked_ticket_ids` as `ticket_id`" in tkt_publish
    assert "sh -c" not in tkt_publish
    assert "printf '%q'" not in tkt_publish


def validate_rendered_outputs(rendered: list[RenderedFile]) -> None:
    banned = ["sh -c", "printf '%q'", "shlex.quote"]
    for item in rendered:
        for needle in banned:
            if needle in item.content:
                raise SystemExit(f"{item.output.relative_to(REPO_ROOT)} contains `{needle}`")
        if item.output.suffix == ".json":
            json.loads(item.content)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="Check for stale generated files")
    args = parser.parse_args()

    operations = load_yaml(OPERATIONS_PATH)
    registry = load_yaml(REGISTRY_PATH)
    adapters = load_named_yaml(ADAPTERS_DIR, "adapter")
    runtimes = load_named_yaml(RUNTIMES_DIR, "runtime")

    validate_registry(registry)
    for adapter in adapters.values():
        validate_adapter(operations, adapter)
    run_golden_tests(operations, adapters)

    rendered = render_all(registry, operations, adapters, runtimes)
    validate_rendered_outputs(rendered)

    changed: list[Path] = []
    for item in rendered:
        if write_rendered_file(item.output, item.content, check=args.check):
            changed.append(item.output)

    if args.check and changed:
        print("Generated files are stale:")
        for path in changed:
            print(f"- {path.relative_to(REPO_ROOT)}")
        return 1

    action = "Checked" if args.check else "Rendered"
    print(f"{action} {len(rendered)} file(s).")
    if changed and not args.check:
        for path in changed:
            print(f"- {path.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
